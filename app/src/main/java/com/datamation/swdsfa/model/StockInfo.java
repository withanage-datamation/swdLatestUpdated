package com.datamation.swdsfa.model;

public class StockInfo {

    private String Stock_Id;
    private String Stock_Itemcode;
    private String Stock_Itemname;
    private String Stock_Qoh;
    private String Stock_Sizes;
    private String Stock_Pril_Code;

    public String getStock_Pril_Code() {
        return Stock_Pril_Code;
    }

    public void setStock_Pril_Code(String stock_Pril_Code) {
        Stock_Pril_Code = stock_Pril_Code;
    }

    public String getStock_Id() {
        return Stock_Id;
    }

    public void setStock_Id(String stock_Id) {
        Stock_Id = stock_Id;
    }

    public String getStock_Itemcode() {
        return Stock_Itemcode;
    }

    public void setStock_Itemcode(String stock_Itemcode) {
        Stock_Itemcode = stock_Itemcode;
    }

    public String getStock_Itemname() {
        return Stock_Itemname;
    }

    public void setStock_Itemname(String stock_Itemname) {
        Stock_Itemname = stock_Itemname;
    }

    public String getStock_Qoh() {
        return Stock_Qoh;
    }

    public void setStock_Qoh(String stock_Qoh) {
        Stock_Qoh = stock_Qoh;
    }

    public String getStock_Sizes() {
        return Stock_Sizes;
    }

    public void setStock_Sizes(String stock_Sizes) {
        Stock_Sizes = stock_Sizes;
    }

}
