package com.datamation.swdsfa.model;

public class free {
    private String freeScheme;
    private String saleItem;
    private String issueItem;
    private String fromQty;
    private String toQty;
    private String itemQty;
    private String freeQty;

    public String getFreeScheme() {
        return freeScheme;
    }

    public void setFreeScheme(String freeScheme) {
        this.freeScheme = freeScheme;
    }

    public String getSaleItem() {
        return saleItem;
    }

    public void setSaleItem(String saleItem) {
        this.saleItem = saleItem;
    }

    public String getIssueItem() {
        return issueItem;
    }

    public void setIssueItem(String issueItem) {
        this.issueItem = issueItem;
    }

    public String getFromQty() {
        return fromQty;
    }

    public void setFromQty(String fromQty) {
        this.fromQty = fromQty;
    }

    public String getToQty() {
        return toQty;
    }

    public void setToQty(String toQty) {
        this.toQty = toQty;
    }

    public String getItemQty() {
        return itemQty;
    }

    public void setItemQty(String itemQty) {
        this.itemQty = itemQty;
    }

    public String getFreeQty() {
        return freeQty;
    }

    public void setFreeQty(String freeQty) {
        this.freeQty = freeQty;
    }
}
